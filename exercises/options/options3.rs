struct Point {
    x: i32,
    y: i32,
}

fn main() {
    let y: Option<Point> = Some(Point { x: 100, y: 200 });

    match &y {          //为 y 添加 借用符（&）以避免 所有权 被match消耗
        Some(p) => println!("Co-ordinates are {},{} ", p.x, p.y),
        _ => panic!("no match!"),
    }
    y; // Fix without deleting this line.  // 这里不会再报错，因为 y 的所有权没有被消耗
}
